# 学年別熟語統計
def get_compound_statistics():
    """熟語データベースの統計情報を取得"""
    stats = {
        "grade_1": len(GRADE_1_COMPOUNDS),
        "grade_2": len(GRADE_2_COMPOUNDS),
        "grade_3": len(GRADE_3_COMPOUNDS),
        "grade_4": len(GRADE_4_COMPOUNDS),
        "grade_5": len(GRADE_5_COMPOUNDS),
        "grade_6": len(GRADE_6_COMPOUNDS),
        "junior_high": sum(len(compounds) for compounds in JUNIOR_HIGH_COMPOUNDS.values()),
        "total": 0
    }
    
    stats["total"] = sum(v for k, v in stats.items() if k != "total")
    return stats

# 難易度別に熟語を分類
def classify_compounds_by_difficulty():
    """難易度別に熟語を分類"""
    beginner = GRADE_1_COMPOUNDS + GRADE_2_COMPOUNDS
    intermediate = GRADE_3_COMPOUNDS + GRADE_4_COMPOUNDS
    advanced = GRADE_5_COMPOUNDS + GRADE_6_COMPOUNDS
    native = []
    
    for subject_compounds in JUNIOR_HIGH_COMPOUNDS.values():
        native.extend(subject_compounds)
    
    return {
        "beginner": beginner,
        "intermediate": intermediate,
        "advanced": advanced,
        "native": native
    }

