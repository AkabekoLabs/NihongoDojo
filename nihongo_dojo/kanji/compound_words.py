# 全ての熟語を難易度別に統合
def get_all_compounds_by_difficulty():
    """全ての熟語を難易度別に統合して返す"""
    
    # 教育熟語から取得
    beginner = (
        GRADE_1_COMPOUNDS + 
        GRADE_2_COMPOUNDS + 
        ADDITIONAL_COMPOUNDS["grade_1_2_additional"]
    )
    
    intermediate = (
        GRADE_3_COMPOUNDS + 
        GRADE_4_COMPOUNDS + 
        ADDITIONAL_COMPOUNDS["grade_3_4_additional"]
    )
    
    advanced = (
        GRADE_5_COMPOUNDS + 
        GRADE_6_COMPOUNDS + 
        ADDITIONAL_COMPOUNDS["grade_5_6_additional"]
    )
    
    native = ADDITIONAL_COMPOUNDS["junior_high_additional"]
    
    # 中学校の教科別熟語を追加
    for subject_compounds in JUNIOR_HIGH_COMPOUNDS.values():
        native.extend(subject_compounds)
    
    return {
        "beginner": beginner,
        "intermediate": intermediate,
        "advanced": advanced,
        "native": native
    }

# 最終的な統合データベース
