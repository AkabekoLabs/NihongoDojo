# 統計情報を取得
def get_statistics():
    """熟語データベースの統計情報"""
    stats = {
        "compounds": {
            "beginner": len(COMPOUND_WORDS_BY_GRADE["beginner"]),
            "intermediate": len(COMPOUND_WORDS_BY_GRADE["intermediate"]),
            "advanced": len(COMPOUND_WORDS_BY_GRADE["advanced"]),
            "native": len(COMPOUND_WORDS_BY_GRADE["native"]),
        },
        "single_kanji": {
            "beginner": len(SINGLE_KANJI_WORDS["beginner"]),
            "intermediate": len(SINGLE_KANJI_WORDS["intermediate"]),
            "advanced": len(SINGLE_KANJI_WORDS["advanced"]),
            "native": len(SINGLE_KANJI_WORDS["native"]),
        }
    }
    
    stats["compounds"]["total"] = sum(stats["compounds"].values())
    stats["single_kanji"]["total"] = sum(stats["single_kanji"].values())
    stats["grand_total"] = stats["compounds"]["total"] + stats["single_kanji"]["total"]
    
    return stats

